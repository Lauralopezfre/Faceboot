            SimpleDateFormat formater = new SimpleDateFormat("hh:mm:ss a dd-MMMM-yyyy");
            
            txtAComentarios.append(comentario.getUsuario().getNombre() + ": " + comentario.getTexto() + newline 
                    + "     " +formater.format(comentario.getFechaHora())+ newline);
